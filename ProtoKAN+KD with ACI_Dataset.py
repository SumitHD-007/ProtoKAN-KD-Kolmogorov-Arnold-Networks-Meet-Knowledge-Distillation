def tp_tn_fp_fn_tables(y_true, y_pred, class_names):
    mcm = multilabel_confusion_matrix(y_true, y_pred)
    rows = []
    for idx, cls in enumerate(class_names):
        tn, fp, fn, tp = mcm[idx].ravel()
        rows.append([cls, tp, tn, fp, fn])

    df = pd.DataFrame(rows, columns=["Class", "TP", "TN", "FP", "FN"])
    print("\n=== TP / TN / FP / FN per class ===")
    print(df.to_string(index=False))
    return df

@torch.no_grad()
def evaluate_full(model, loader, name="Model", class_names=None, device=None):
    model.eval()
    if device is None:
        device = next(model.parameters()).device

    y_true, y_pred = [], []
    for xb, yb in loader:
        xb, yb = xb.to(device), yb.to(device)
        try:
            logits = model(xb, yb)
        except TypeError:
            logits = model(xb)
        y_true.append(yb.cpu().numpy())
        y_pred.append(logits.softmax(1).argmax(1).cpu().numpy())

    y_true = np.concatenate(y_true)
    y_pred = np.concatenate(y_pred)

    # Fix class order to match names on both axes
    if class_names is not None:
        labels_order = np.arange(len(class_names))
        tick_names   = class_names
    else:
        labels_order = np.unique(np.concatenate([y_true, y_pred]))
        tick_names   = [str(i) for i in labels_order]

    cm  = confusion_matrix(y_true, y_pred, labels=labels_order)
    acc = accuracy_score(y_true, y_pred)

    print(f"\n=== {name} ===")
    print(f"Accuracy: {acc:.4f}")
    print(
        classification_report(
            y_true, y_pred,
            labels=labels_order,
            target_names=tick_names,
            digits=4
        )
    )

    plt.figure(figsize=(14, 10))
    ax = sns.heatmap(
        cm, annot=True, fmt='d', cmap='Blues',
        xticklabels=tick_names, yticklabels=tick_names
    )
    ax.set_title(f"Confusion Matrix — {name}")
    ax.set_xlabel("Predicted"); ax.set_ylabel("True")
    plt.xticks(rotation=90, ha='center'); plt.yticks(rotation=0)
    plt.tight_layout(); plt.show()
    # Add TP/TN/FP/FN table
    tp_tn_fp_fn_tables(y_true, y_pred, tick_names)

    return acc

