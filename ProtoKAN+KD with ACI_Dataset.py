class KANEncoder(nn.Module):
    def __init__(self, input_dim, hidden_dims, out_dim=128):
        super().__init__()
        layers_hidden = [input_dim] + hidden_dims + [out_dim]
        self.kan = KAN(
            layers_hidden=layers_hidden,
            grid_size=5,
            spline_order=3,
            scale_noise=0.1,
            scale_base=1.0,
            scale_spline=1.0,
            base_activation=torch.nn.SiLU,
            grid_eps=0.02,
            grid_range=[-1, 1],
        )
    def forward(self, x):
        return self.kan(x)

class ProtonetKAN(nn.Module):
    def __init__(self, encoder, n_classes):
        super().__init__()
        self.encoder = encoder
        self.n_classes = n_classes

    def compute_prototypes(self, embeddings, labels):
        prototypes = []
        for c in range(self.n_classes):
            mask = (labels == c)
            emb = embeddings[mask]
            if emb.size(0) == 0:
                prototypes.append(torch.zeros(embeddings.size(1), device=embeddings.device))
            else:
                prototypes.append(emb.mean(dim=0))
        return torch.stack(prototypes)

    def forward(self, x, labels):
        embeddings = self.encoder(x)
        prototypes = self.compute_prototypes(embeddings, labels)
        dists = ((embeddings.unsqueeze(1) - prototypes.unsqueeze(0)) ** 2).sum(-1)
        return -dists  # logits

def train_teacher(model, train_loader, optimizer, n_epochs=30, device=DEVICE, name="Teacher"):
    model.train()
    for epoch in range(1, n_epochs + 1):
        losses, all_preds, all_targets = [], [], []
        for xb, yb in train_loader:
            xb, yb = xb.to(device), yb.to(device)
            logits = model(xb, yb)
            loss = F.cross_entropy(logits, yb)
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            losses.append(loss.item())
            preds = logits.softmax(dim=1).argmax(dim=1)
            all_preds.append(preds.cpu().numpy())
            all_targets.append(yb.cpu().numpy())

        epoch_acc = accuracy_score(np.concatenate(all_targets), np.concatenate(all_preds))
        print(f'[{name}] Epoch {epoch:02d} | Loss: {np.mean(losses):.4f} | Acc: {epoch_acc:.4f}')

def distill_student_multi_teacher(
    teacher1, teacher2, student, loader, optimizer,
    n_epochs=50, device='cuda' if torch.cuda.is_available() else 'cpu', temperature=4.0, alpha=0.5
):
    """
    alpha = weight on hard CE; (1-alpha) on KD. (If you want logit-only, set alpha=0.0)
    """
    T = temperature
    student.train()
    for _ in range(n_epochs):
        for xb, yb in loader:
            xb, yb = xb.to(device), yb.to(device)
            optimizer.zero_grad()

            with torch.no_grad():
                t1 = teacher1(xb, yb)
                t2 = teacher2(xb, yb)
                soft_t = 0.5*F.softmax(t1/T, dim=1) + 0.5*F.softmax(t2/T, dim=1)

            s = student(xb, yb)

            kd = F.kl_div(
                F.log_softmax(s/T, dim=1),
                soft_t,
                reduction="batchmean"
            ) * (T*T)

            hard = F.cross_entropy(s, yb)
            loss = (1 - alpha) * kd + alpha * hard

            loss.backward()
            optimizer.step()

