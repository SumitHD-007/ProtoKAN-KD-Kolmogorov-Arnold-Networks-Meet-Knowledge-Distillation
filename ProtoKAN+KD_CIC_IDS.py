best_state_raw, best_state_ema = None, None
total_kd_losses = []
for epoch in range(1, KD_EPOCHS + 1):
    student.train()
    batch_losses = []
    for xb, yb in train_loader:
        xb, yb = xb.to(DEVICE), yb.to(DEVICE)
        opt_student.zero_grad()

        with torch.no_grad():
            t1 = teacher1(xb, yb)
            t2 = teacher2(xb, yb)
            soft_t = 0.5 * F.softmax(t1 / KD_T, dim=1) + 0.5 * F.softmax(t2 / KD_T, dim=1)

        s = student(xb, yb)
        kd   = F.kl_div(F.log_softmax(s / KD_T, dim=1), soft_t, reduction="batchmean") * (KD_T * KD_T)
        hard = F.cross_entropy(s, yb)
        loss = (1 - KD_ALPHA) * kd + KD_ALPHA * hard

        loss.backward()
        sch_student.step()
        opt_student.step()
        batch_losses.append(loss.item())
        with torch.no_grad():
          epoch_loss = np.mean(batch_losses)
          total_kd_losses.append(epoch_loss)

    # ---------- evaluate only every N epochs ----------
    if (epoch % EVAL_EVERY == 0) or (epoch == KD_EPOCHS):
        acc_raw = evaluate_full(student, test_loader, name=f"Student RAW (epoch {epoch})",
                                class_names=attack_names, device=DEVICE)
        if acc_raw > best_acc_raw:
            best_acc_raw = acc_raw
            best_state_raw = {k: v.detach().cpu().clone() for k, v in student.state_dict().items()}

        ema.apply_shadow(student)
        acc_ema = evaluate_full(student, test_loader, name=f"Student EMA (epoch {epoch})",
                                class_names=attack_names, device=DEVICE)
        if acc_ema > best_acc_ema:
            best_acc_ema = acc_ema
            best_state_ema = {k: v.detach().cpu().clone() for k, v in student.state_dict().items()}
        ema.restore(student)
# ------------------------------------------------------

# Load the better of RAW/EMA
final_state = best_state_ema if best_state_ema is not None else best_state_raw
if final_state is not None:
    student.load_state_dict(final_state)
print(f"\n[Student KD] Loaded best {'EMA' if best_state_ema is not None else 'RAW'} weights.")

# Final evaluation (names on axes & report)
final_acc = evaluate_full(student, test_loader, name="Knowledge Distilled Student ProtoKAN Model Confusion Matrix with Per-Class F1-score", class_names=attack_names, device=DEVICE)
print(f"\n>>> MAIN Student Accuracy: {final_acc*100:.2f}%")

