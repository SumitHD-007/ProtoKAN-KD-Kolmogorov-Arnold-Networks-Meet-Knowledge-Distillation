class ProtonetKAN(nn.Module):
    def __init__(self, encoder, n_classes):
        super().__init__()
        self.encoder = encoder
        self.n_classes = n_classes

    def compute_prototypes(self, embeddings, labels):
        prototypes = []
        for c in range(self.n_classes):
            mask = (labels == c)
            emb = embeddings[mask]
            if emb.size(0) == 0:
                prototypes.append(torch.zeros(embeddings.size(1), device=embeddings.device))
            else:
                prototypes.append(emb.mean(dim=0))
        return torch.stack(prototypes)

    def forward(self, x, labels):
        embeddings = self.encoder(x)
        prototypes = self.compute_prototypes(embeddings, labels)
        dists = ((embeddings.unsqueeze(1) - prototypes.unsqueeze(0)) ** 2).sum(-1)
        return -dists  # logits

