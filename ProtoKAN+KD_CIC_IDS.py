def train_teacher(model, train_loader, optimizer, n_epochs=30, device=DEVICE, name="Teacher"):
    model.train()
    for epoch in range(1, n_epochs + 1):
        losses, all_preds, all_targets = [], [], []
        for xb, yb in train_loader:
            xb, yb = xb.to(device), yb.to(device)
            logits = model(xb, yb)
            loss = F.cross_entropy(logits, yb)
            optimizer.zero_grad()
            loss.backward()
            optimizer.step()

            losses.append(loss.item())
            preds = logits.softmax(dim=1).argmax(dim=1)
            all_preds.append(preds.cpu().numpy())
            all_targets.append(yb.cpu().numpy())

        epoch_acc = accuracy_score(np.concatenate(all_targets), np.concatenate(all_preds))
        print(f'[{name}] Epoch {epoch:02d} | Loss: {np.mean(losses):.4f} | Acc: {epoch_acc:.4f}')

