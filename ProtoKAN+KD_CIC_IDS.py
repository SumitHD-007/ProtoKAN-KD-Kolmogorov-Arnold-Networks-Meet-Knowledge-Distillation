def make_cosine_warmup(optimizer, num_epochs, warmup_ratio=0.1):
    warm = max(1, int(warmup_ratio * num_epochs))
    def lr_lambda(epoch):
        if epoch < warm:
            return float(epoch + 1) / float(warm)
        prog = (epoch - warm) / max(1, (num_epochs - warm))
        return 0.5 * (1.0 + math.cos(math.pi * prog))
    return torch.optim.lr_scheduler.LambdaLR(optimizer, lr_lambda)

class ModelEMA:
    def __init__(self, model, decay=0.999, device=None):
        self.decay = decay
        self.shadow = {}
        self.backup = {}
        for name, param in model.named_parameters():
            if param.requires_grad:
                self.shadow[name] = param.data.clone()
        self.device = device

    @torch.no_grad()
    def update(self, model):
        for name, param in model.named_parameters():
            if param.requires_grad:
                self.shadow[name] = self.decay * self.shadow[name] + (1.0 - self.decay) * param.data

    @torch.no_grad()
    def apply_shadow(self, model):
        self.backup = {}
        for n, p in model.named_parameters():
            if p.requires_grad:
                self.backup[n] = p.data.clone()
                p.data.copy_(self.shadow[n])

    @torch.no_grad()
    def restore(self, model):
        for n, p in model.named_parameters():
            if n in self.backup and p.requires_grad:
                p.data.copy_(self.backup[n])
        self.backup = {}

